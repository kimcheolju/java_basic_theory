package variable;

/*
변수의 변경
 */
public class Var3 {
    public static void main(String[] args) {
        int a; //변수 선언
        a = 10; //변수 초기화 a(10) 1. 변수 a에 10을 저장한다.
        System.out.println(a); // 2. 변수 a의 값을 읽는다.
        a = 50; //변수 값 변ㄱ셩 a(10->50) 3. 변수 a에 저장된 값 10을 버리고 50을 저장한다.
        System.out.println(a); // 4. 변수 a의 값을 읽는다.
    }
}
